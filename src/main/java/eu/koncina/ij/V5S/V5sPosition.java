package eu.koncina.ij.V5S;

public class V5sPosition {
	int c = 0;
	int z = 0;
	int t = 0;

	public V5sPosition() {

	}

	public V5sPosition(int c, int z, int t) {
		this.c = c;
		this.z = z;
		this.t = t;
	}

	public int getC() {
		return this.c;
	}

	public int getZ() {
		return this.z;
	}

	public int getT() {
		return this.t;
	}

}